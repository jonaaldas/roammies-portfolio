		</p>

		<h3 class="text-xl">1. Choose a Destination or Work Backwards</h3>
		<p>If daycare is a priority for you, then chances are you’re only willing to consider destinations that have this option available for your family.</p>
		<p>
			This means that the first step in finding short-term daycare abroad is to decide whether or not you want to choose your favorite destinations and find what’s available there, or work
			backwards: search for daycares that meet your needs around the world and be open to choosing a destination based on that. This may mean changing some of your plans a bit, but it can also
			help open more doors in general.
		</p>
		<h3 class="text-xl">2. Understand the Terminology: Decoding “Childcare” Worldwide</h3>
		<p>
			When you are searching, it’s important to keep in mind that although the term “short-term daycare abroad” makes sense depending on where you’re from, the reality is that the terms used to
			describe this type of childcare option are different all over the world. The terms used may also differ depending on the child’s age, the language, the audience, etc.
		</p>
		<p>
			“Daycare” for instance may bring up images of germy environments with loads of babies, while “creche” might sound more sophisticated. “Nursery” in some countries may refer to a place for
			newborns, whereas the term “nursery school” may refer to a school for 2-4 year-olds. But, generally—regardless of the specific term used—they all have the same purpose: a place to bring
			your child to be cared for and socialize with other children.
		</p>
		<p>All this considered, we find it helpful to categorize these terms as best as possible:</p>

		<div class="space-y-3">
			<p>
				<strong><u>Program Availability:</u></strong>
			</p>
			<ul class="list-disc pl-3">
				<li>
					<strong>Short-term:</strong>
					Commonly used in North America, daycare facilities offer supervised care for infants and young children during the day, usually 0 - 36 months, while preschool may be for ages 3-6.
					It will vary on the country and school, and of course, each school will have its language to refer to this (see ‘school’ below).
				</li>
				<li>
					<strong>Open-enrollment:</strong>
					Programs that allow children to enroll on an as-needed basis without long-term commitments, or they can enroll outside of normal enrollment periods. This may be an attractive
					option to families who aren’t sure how long they may want to stay somewhere but want to ensure they can enroll their child at any time during the year for the foreseeable
					future. No need to operate under strict sign-up windows.
				</li>
				<li>
					<strong>Flexible:</strong>
					Flexible daycare in the broader context generally means part-time enrollment, whether it’s a few days a week and/or a few hours a day. But, “flexible” can also mean for a few weeks
					or months at a time, or as needed. Keep in mind that while some short-term daycares offer flexible options, not all daycares with flexible options allow short-term
					enrollment. You’ll have to check what the school means when they say “flexible” and no it could be either/or both.
				</li>
				<li>
					<strong>Drop-in:</strong>
					Programs where parents can bring their children at random or pre-scheduled times. The program may require the parents to stay for the program (for instance, a parent-child music
					class or yoga class), or have supervision for some time (for instance, a drop-off playgroup, or a babysitting service at a gym), allowing parents some free time.
				</li>
				<li>
					<strong>Hybrid:</strong>
					Schools that offer one or more of the above and/or on a case-by-case basis.
				</li>
			</ul>
		</div>

		<div class="space-y-3">
			<p>
				<strong>
					<u>Type:</u>
				</strong>
			</p>
			<ul class="list-disc pl-3">
				<li>
					<strong>School:</strong>
					“School” can encompass different facilities that offer supervised care for infants and babies, toddlers, and pre-kindies during the day. Usually, the ages can range from 0 up to 6
					years, with different groups within this including “daycare”, “nursery”, “preschool”, “kindergarten”, and more. You’ll have to do additional research to understand what’s meant by
					the term your prospective “school” uses.
				</li>
				<li>
					<strong>Co-working + Childcare:</strong>
					Many co-working spaces are popping up around the world that offer babysitting and/or more consistent daycare. This is a great option for digital nomad parents who need to work but
					want to be near their babies.
				</li>
				<li>
					<strong>Indoor Playground:</strong>
